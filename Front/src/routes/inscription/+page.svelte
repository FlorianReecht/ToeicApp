<h1>INSCRIPTION TOEIC</h1>

<h2>Pour s'inscrire au TOEIC : </h2> 

<p> 
    En général, un mail est envoyé par Alain Roussel quelques jours avant la fin de l'inscription au TOEIC. <br>
    Le tarif pour l'inscription est de 43€, cependant si c'est votre première inscription, elle est gratuite ! <br>

    Vous pouvez soit régler le montant par chèque libellé à <strong>l'ordre de l'agent comptable de l'Université de Lorraine</strong> et indiquant au verso du chèque : <br>
    <li> - Nom </li>
    <li>- Prénom</li>
    <li>- N° d'étudiant</li>
    <li>- Année</li>
    <br>
    Ensuite vous pouvez soit l'envoyer par courrier au service scolarité, 2 rue Jean Lamour - 54519 Vandoeuvre-lès-Nancy Cedex ou le déposer directement à la scolarité. 
    <br>
    Soit payer par carte bancaire directement à la scolarité. <br>

    Il faut aussi fournir une enveloppe (format 21 * 11 cm) affranchie au tatif en vigueur, avec au recto : nom, prénom, adresse et code postal pour recevoir le résultat. 
</p>